public class WalletGroupListItem {

    private String name;
    private String test;

    public WalletGroupListItem(String name, String test) {
        super();
        this.name = name;
        this.test = test;
    }

    public String getName() {
        return this.name;
    }

    public String getTest() {
        return this.test;
    }
}